#include <stdio.h>
//Write a Program to Print the Average of 3
int main() {
    int x,y,z;
    printf("Enter 1st number : ");
    scanf("%d",&x);
    printf("Enter 2nd number : ");
    scanf("%d",&y);
    printf("Enter 3rd number : ");
    scanf("%d",&z);
    printf("%d",(x+y+z)/3);
    return 0;
}